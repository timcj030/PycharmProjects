
strval = input('Enter a string: ')
print('Your entered string {0}'.format(strval))

print('Applying method lower() on string {0}'.format(strval))
print('Result is {0}'.format(strval.lower()))

print('Applying method upper() on string {0}'.format(strval))
print('Result is {0}'.format(strval.upper()))

print('Applying method title() on string {0}'.format(strval))
print('Result is {0}'.format(strval.title()))

print('Applying method swapcase() on string {0}'.format(strval))
print('Result is {0}'.format(strval.swapcase()))



